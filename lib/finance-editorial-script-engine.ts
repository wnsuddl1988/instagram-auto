function scriptLines(value: string): string[] {
  return String(value ?? "")
    .split(/\r?\n/u)
    .map((line) => line.replace(/\s+/gu, " ").trim())
    .filter(Boolean);
}

function compactCoverSubject(title: string, maxChars = 13): string {
  const clean = title
    .replace(/[.!?。！？]+$/gu, "")
    .replace(/\s+(?:이유|방법|기준|신호|습관)$/u, "")
    .trim();
  if ([...clean].length <= maxChars) return clean;
  const words = clean.split(/\s+/u);
  const selected: string[] = [];
  for (const word of words) {
    const next = [...[...selected, word].join(" ")].length;
    if (selected.length > 0 && next > maxChars) break;
    selected.push(word);
  }
  const result = selected.join(" ").trim();
  return result || [...clean].slice(0, maxChars).join("");
}


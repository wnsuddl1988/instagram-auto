/** 500개 소진 뒤 생성되는 새 제목도 같은 9개 편집 흐름으로 보낸다. */
export function inferFinanceEditorialLane(title: string, angle?: string): FinanceEditorialLane {
  const text = `${title} ${angle ?? ""}`.replace(/\s+/g, " ").trim();
  if (/부자|성공하는 사람|돈을 지키는 사람|자산을 키우는 사람|돈 모으는 사람/.test(text)) return "wealth_standard";
  if (/숫자|금액|비율|총이자|개월 수|몇 달|세후|실수령액/.test(text) || angle === "숫자") return "number_gap";
  if (/가장 먼저|전에 반드시|먼저 해야|바로 해야|끊어야|버려야|확인할|세워야|정해야|할 일/.test(text) || angle === "루틴" || angle === "꿀팁") {
    return "action_one";
  }
  if (/실패한 건 아니다|늦어도|되찾|괜찮|필요는 없다|할 수 있다|포기하지/.test(text)) return "recovery";
  if (/신호|결정적 순간|뉴스와 .*다른|전에 .*흔들|오르기 전에|상승이 .*번지|경제 변화/.test(text)) return "economic_signal";
  if (/위험|끝이다|당한다|사라진다|못 버틴다|가져간다|커진다|넘기 시작|경고/.test(text)) return "warning";
  if (/사람|습관|착각|보는 순간|만 하는|그대로 두는/.test(text) || angle === "실수") return "habit_exposure";
  if (/올랐는데|늘었는데|줄였는데|샀는데|했는데|인데도|지만|오히려|줄지|안 .*이유/.test(text) || angle === "반전") return "reversal";
  return "psychology_gap";
}

function resolveProfile(topic: FinanceEditorialTopic): ScriptProfile & { profileId: string } {
  const base = BASE_PROFILES[topic.financeSubtopic];
  const override = PROFILE_OVERRIDES[topic.financeSubtopic].find((candidate) => candidate.pattern.test(topic.title));
  if (!override) return { ...base, profileId: `${topic.financeSubtopic}:default` };
  return {
    ...base,
    ...override,
    profileId: `${topic.financeSubtopic}:${override.id}`,
  };
}

export function buildFinanceEditorialScriptParts(topic: FinanceEditorialTopic): FinanceEditorialScriptParts {
  const profile = resolveProfile(topic);
  const lane = LANE_GUIDANCE[topic.lane];
  return {
    hook: beat(topic.title, ...profile.hook),
    situation: beat(...profile.situation),
    consequence: beat(...profile.consequence),
    psychology: beat(...profile.psychology, ...lane.psychology),
    mindset: beat(...profile.standard, ...lane.standard),
    habit: beat(...profile.action),
    recommendation: beat(
      ...profile.recall,
      "다음 선택 전에 바로 꺼내 볼 수 있게 저장해 둬",
      DOMAIN_FOLLOW[topic.financeSubtopic],
    ),
    profileId: profile.profileId,
    focus: profile.focus,
  };
}

